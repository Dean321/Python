def sum(a,b):
    return a+b
def diff(a,b):
    return a-b
def pro(a,b):
    return a*b
def quot(a,b):
    return a/b
opt = {
          "+":sum,
          "-":diff,
          "*":pro,
          "/":quot
      }
choice = "n"
ans = 0
while choice!="e":
    if choice=="n":
        ans = 0
        num1 = float(input("Enter 1st number: "))
    else:
        num1 = ans
    op = input("Choose operator\n+\n-\n*\n/\n")
    num2 = float(input("Enter 2nd number: "))
    ans = opt[op](num1,num2)
    print(f"{num1}{op}{num2}={ans}")
    choice = input("Do wish to continue with same calculation type y-yes n-no and to exit program type e-exit")