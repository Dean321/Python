def ceaser(text, shift, direction):
    ans=""
    for a in text:
        index = alphabet.index(a)
        if direction=="encode":
              i = shift + index
              while(i>=len(alphabet)):
                  l = len(alphabet)
                  i = i-l
        else:
              i = index - shift
              while i<0:
                  i = (len(alphabet)-0)+i
        
        ans+=alphabet[i]  
    return ans
